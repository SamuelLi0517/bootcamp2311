package DemoPrimitives;

public class DemoDouble {
  public static void main(String[] args) {

    double d1 = 0.3;
    double d2 = 0.2;
    double d3 = d1 + d2;
    System.out.println(d3); // 0.5

    double d4 = 19999.31123912302110109;
    System.out.println(d4); // 19999.31123912302

    float f1 = 0.2f; // f is notation of float

    float f2 = 19999.31123912302110109f;
    System.out.println(f2); // 19999.31
    

  }
  
}
